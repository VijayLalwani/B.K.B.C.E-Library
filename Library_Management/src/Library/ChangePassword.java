    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String UID = Old_ID.getText();
        String nuid = New_ID.getText();
        String PWD = Prev_Pass.getText();
        String nPWD = New_Pass.getText();
        String rnPWD = Re_New_Pass.getText();
        String a = null,b = null;
        if (UID.equals("") || nuid.equals("") || PWD.equals("") || nPWD.equals("") || rnPWD.equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill all the fields");
        } else {
            Statement stmt;
            Connection conn;
            String sql;
            try {
                Class.forName("com.mysql.jdbc.Driver");
                conn = (Connection)DriverManager.getConnection(db_url,user,pwd);
                stmt = (Statement)conn.createStatement();
                sql = "select * from users;";
                ResultSet rs = stmt.executeQuery(sql);
                rs.next();
                a = rs.getString("id");
                b = rs.getString("pass");
                rs.close();
            }
            catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getLocalizedMessage());
            }
            if (nPWD.equals(rnPWD)) {
                if (UID.equals(a) && PWD.equals(b)) {
                    try {
                        Class.forName("com.mysql.jdbc.Driver");
                        conn = (Connection)DriverManager.getConnection(db_url,user,pwd);
                        stmt = (Statement)conn.createStatement();
                        sql = "update users set id ='" + nuid + "',pass = '" + nPWD + "' where id = '" + a + "'; ";
                        stmt.executeUpdate(sql);
                        stmt.close();
                        conn.close();
                        JOptionPane.showMessageDialog(null, "modified Sucessfully");
                    }
                    catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e.getLocalizedMessage());
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "your id or password is in correct");
                }
            } else {
                JOptionPane.showMessageDialog(null, "password and re-enter password are not same");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

